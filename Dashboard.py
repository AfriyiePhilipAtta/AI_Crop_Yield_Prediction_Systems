import streamlit as st            # Streamlit web app framework
import rasterio                   # For reading GeoTIFF raster data
import numpy as np                # Numerical computations
import matplotlib.pyplot as plt   # For plotting maps
import os                         # File and path operations

# ============================================================
# PAGE CONFIG
# ============================================================
# Sets up Streamlit page title and layout
st.set_page_config(
    page_title="AI Crop Yield Dashboard",  # Browser tab title
    layout="wide"                          # Use full page width
)

st.title("🌾 AI Crop Yield Prediction Dashboard")  # Main title of dashboard

# ============================================================
# PATHS (must match pipeline output)
# ============================================================
# Base directory is where script is located
BASE_DIR = os.path.dirname(os.path.abspath(__file__))
OUTPUT_DIR = os.path.join(BASE_DIR, "output")  # Folder where pipeline outputs are stored

# Paths to the GeoTIFF files generated by the AI crop yield pipeline
NDVI_MAP = os.path.join(OUTPUT_DIR, "NDVI_Map.tif")
YIELD_MAP = os.path.join(OUTPUT_DIR, "Yield_Map.tif")
UNCERTAINTY_MAP = os.path.join(OUTPUT_DIR, "Yield_Uncertainty_Map.tif")

# ============================================================
# HELPER FUNCTIONS
# ============================================================
def load_raster(path):
    """
    Load a raster file as a numpy array.
    Replaces nodata values with np.nan for easier calculations.
    """
    with rasterio.open(path) as src:
        arr = src.read(1).astype("float32")  # Read first band
        nodata = src.nodata                   # Get nodata value

    # Replace nodata values with NaN
    if nodata is not None:
        arr[arr == nodata] = np.nan

    return arr


def plot_raster(arr, title, cmap, vmin=None, vmax=None):
    """
    Plot a raster array with matplotlib and display it in Streamlit.
    - arr: 2D numpy array
    - title: plot title
    - cmap: color map
    - vmin, vmax: optional min/max values for color scaling
    """
    fig, ax = plt.subplots(figsize=(6, 6))
    im = ax.imshow(arr, cmap=cmap, vmin=vmin, vmax=vmax)  # Display raster
    ax.set_title(title)
    ax.axis("off")  # Hide axes
    plt.colorbar(im, ax=ax, shrink=0.75)  # Add colorbar
    st.pyplot(fig)  # Render figure in Streamlit

# ============================================================
# CHECK OUTPUT FILES
# ============================================================
# Ensure that all required files exist before loading
missing = [p for p in [NDVI_MAP, YIELD_MAP, UNCERTAINTY_MAP] if not os.path.exists(p)]
if missing:
    st.error("❌ Output files not found. Please run the pipeline first.")  # Alert user
    st.stop()  # Stop execution if files are missing

# ============================================================
# LOAD DATA
# ============================================================
# Load NDVI, yield, and uncertainty maps as numpy arrays
ndvi = load_raster(NDVI_MAP)
yield_map = load_raster(YIELD_MAP)
uncertainty = load_raster(UNCERTAINTY_MAP)

# ============================================================
# METRICS
# ============================================================
# Calculate mean yield and uncertainty for display
mean_yield = np.nanmean(yield_map)
mean_uncertainty = np.nanmean(uncertainty)

# Create three columns to display metrics
c1, c2, c3 = st.columns(3)

c1.metric("Mean Field Yield (t/ha)", f"{mean_yield:.2f}")           # Average yield
c2.metric("Mean Uncertainty (t/ha)", f"± {mean_uncertainty:.2f}")   # Average absolute uncertainty
c3.metric(
    "Relative Uncertainty (%)",
    f"{100 * mean_uncertainty / mean_yield:.1f}"                    # Relative uncertainty in %
)

st.markdown("---")  # Horizontal line separator

# ============================================================
# MAPS
# ============================================================
# Display NDVI, yield, and uncertainty maps side by side
c1, c2, c3 = st.columns(3)

with c1:
    plot_raster(
        ndvi,
        "NDVI Composite",
        cmap="YlGn",  # Yellow-to-Green for vegetation
        vmin=0,
        vmax=1
    )

with c2:
    plot_raster(
        yield_map,
        "Yield Map (t/ha)",
        cmap="viridis"  # Viridis colormap for yield
    )

with c3:
    plot_raster(
        uncertainty,
        "Yield Uncertainty (t/ha)",
        cmap="magma"   # Magma colormap for uncertainty
    )

# ============================================================
# FOOTER
# ============================================================
st.markdown("---")
st.caption(
    "NDVI derived from Sentinel‑2 (cloud‑free median composite). "
    "Yield predicted using XGBoost with weather and SAR features."
)
